import Foundation

// MARK: - String+Handlers
//
extension String {
  
  /// Int value
  ///
  var intValue: Int? {
    return Int(self)
  }
  
  /// Double value
  ///
  var doubleValue: Double? {
    return Double(self)
  }
}
